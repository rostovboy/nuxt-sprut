<template>
  <div v-if="product">
    <button
      v-if="!isProductAdded"
      :class="$style.buy"
      @click.prevent="buyClickHandler"
    >
      Купить
    </button>
    <a
      v-else
      :class="$style.added"
      href="#"
      title="Товар в корзине. Удалить?"
      @click.prevent="addedClickHandler"
    >
      <span class="added-btn">
        <font-awesome-icon :icon="['fas', 'check']"/>
      </span>
      <span class="remove-btn" style="display: none">
        <font-awesome-icon  :icon="['fas', 'trash-alt']"/>
      </span>
    </a>
  </div>
</template>

<script>
import { mapActions, mapState } from 'vuex'

export default {
  props: {
    product: {
      type: Object,
      required: true
    }
  },
  computed: {
    ...mapState({
      products: state => state.cart.products
    }),
    isProductAdded () {
      return this.products.find(p => p.id === this.product.id)
    }
  },
  methods: {
    ...mapActions({
      addProduct: 'cart/addProduct',
      removeProduct: 'cart/removeProduct'
    }),
    buyClickHandler () {
      this.addProduct(this.product)
    },
    addedClickHandler () {
      this.removeProduct(this.product.id)
    }
  }
}
</script>

